#!/bin/bash

root_dir=<absolute_path_to_the_root_of_this_repository>
save_dir=baselines/copur/cifar100

dataset=CIFAR100
datapath=<point_to_your_dataset_path>
seeds=(90)
alphas=(0.5)
proxy_ratio=0.1
sizes=(17)
encode_dim=800
amplifications=(1 2 4 8 16)
n_advs=(1)

env_python=<point_to_your_conda_python_interpreter>

declare -A final_iters_map
final_iters_map=(["1"]=10 ["2"]=20 ["3"]=30 ["4"]=40 )

for size in "${sizes[@]}"; do
    
    for alpha in "${alphas[@]}"; do

        for seed in "${seeds[@]}"; do
            
            for amplification in "${amplifications[@]}"; do

                for n_adv in "${n_advs[@]}"; do

                    final_iters=${final_iters_map[$n_adv]}

                    echo "==> Running test for amplification: $amplification, n_adv: $n_adv final_iters: $final_iters"
                
                    name=${dataset}_${alpha}_M${size}_${seed}_test_amp${amplification}_nadv${n_adv}_fi${final_iters}
                    # to save the results of the aggregation training
                    log_dir=${root_dir}/results/${save_dir}/${name}
                    mkdir -p $log_dir

                    name2=${dataset}_${alpha}_M${size}_${seed}_${proxy_ratio}
                    # path to the logits generated by the local training
                    datapath=${root_dir}/results/logits/${name2}

                    name3=${dataset}_${alpha}_M${size}_${seed}_edim${encode_dim}_AE_train
                    # path to the saved autoencoder model
                    modelpath=${root_dir}/results/${save_dir}/${name3}

                    name4=${dataset}_${alpha}_M${size}_${seed}_edim${encode_dim}_server_model_train
                    modelpath2=${root_dir}/results/${save_dir}/${name4}

                    # count time for experiment in hh mm ss
                    start=`date +%s`

                    $env_python $root_dir/Baselines/CoPur/test.py \
                        --size $size \
                        --dataset $dataset \
                        --datapath $datapath \
                        --modelpath $modelpath \
                        --modelpath2 $modelpath2 \
                        --partition_method dirichlet \
                        --save_dir $log_dir \
                        --alpha $alpha \
                        --seed $seed \
                        --batch_size 256 \
                        --hidden_dim 2000 \
                        --gpu \
                        --encode_dim $encode_dim \
                        --amplification $amplification \
                        --lr 1e-1 \
                        --final_iters $final_iters \
                        --optimizer Adam \
                        --attack_type dfl \
                        --n_adv $n_adv \
                        --eval_one_adv

                    end=`date +%s`
                    runtime=$((end-start))
                    # Print time in hh:mm:ss
                    echo "==> Time taken: $(($runtime / 3600 )):$((($runtime / 60) % 60)):$(( $runtime % 60 ))"

                done
            done
        done
    done
done
