#!/bin/bash

root_dir=<absolute_path_to_the_root_of_this_repository>
save_dir=summer/agg_training_adv/cifar100

env_python=<point_to_your_conda_python_interpreter>

dataset=CIFAR100
datapath=/scratch/shared/datasets
seeds=(94)
proxy_ratio=0.1
alphas=(0.5)
model=DeepSet_M
trim_ratio=0.25
sizes=(17)
black_box=false
collude=true
attack_type=pgd
n_subsets=300

if [ "$black_box" = true ]; then
    black_box_str="--black_box"
else
    black_box_str=""
fi

if [ "$collude" = true ]; then
    collude_str="--collude"
else
    collude_str=""
fi

for size in "${sizes[@]}"; do
    
    for alpha in "${alphas[@]}"; do

        for seed in "${seeds[@]}"; do

            maxss=$((size))
            minss=$((size-4)) # set min set size to size - f (i.e. max number of attackers)
            
            name=${dataset}_${alpha}_M${size}_${seed}_${proxy_ratio}_${model}_maxss${maxss}_minss${minss}_trim${trim_ratio}_attack${attack_type}_bb${black_box}_collude${collude}_nsubsets${n_subsets}_cw
            # to save the results of the aggregation training
            log_dir=${root_dir}/results/${save_dir}/${name}
            mkdir -p $log_dir

            name2=${dataset}_${alpha}_M${size}_${seed}_${proxy_ratio}
            # path to the logits generated by the local training
            datapath=${root_dir}/results/logits/${name2}

            # count time for experiment in hh mm ss
            start=`date +%s`

            $env_python $root_dir/aggregator_training_fl.py \
                --size $size \
                --model $model \
                --dataset $dataset \
                --datapath $datapath \
                --partition_method dirichlet \
                --save_dir $log_dir \
                --alpha $alpha \
                --seed $seed \
                --proxy_ratio $proxy_ratio \
                --batch_size 256 \
                --gpu \
                --dim_hidden 224 \
                --debug \
                --normalize \
                --lr 5e-5 \
                --loss_fn cw \
                --epochs 10 \
                --optimizer Adam \
                --add_subsets \
                --n_subsets $n_subsets \
                --max_set_size $maxss \
                --min_set_size $minss \
                --trim_ratio $trim_ratio \
                --adversarial \
                --attack_type $attack_type $black_box_str $collude_str

            end=`date +%s`
            runtime=$((end-start))
            # Print time in hh:mm:ss
            echo "==> Time taken: $(($runtime / 3600 )):$((($runtime / 60) % 60)):$(( $runtime % 60 ))"
        
        done
    done
done
