#!/bin/bash

root_dir=<absolute_path_to_the_root_of_this_repository>
save_dir=summer/baselines/copur

env_python=<point_to_your_conda_python_interpreter>

dataset=CIFAR100
datapath=<point_to_your_dataset_path>
seeds=(90)
alphas=(0.5)
proxy_ratio=0.1
sizes=(17)
encode_dim=800

for size in "${sizes[@]}"; do
    
    for alpha in "${alphas[@]}"; do

        for seed in "${seeds[@]}"; do

            name=${dataset}_${alpha}_M${size}_${seed}_edim${encode_dim}_AE_train
            # to save the results of the aggregation training
            log_dir=${root_dir}/results/${save_dir}/${name}
            mkdir -p $log_dir

            name2=${dataset}_${alpha}_M${size}_${seed}_${proxy_ratio}
            # path to the logits generated by the local training
            datapath=${root_dir}/results/logits/${name2}

            # count time for experiment in hh mm ss
            start=`date +%s`

            $env_python $root_dir/Baselines/CoPur/train_ae.py \
                --size $size \
                --dataset $dataset \
                --datapath $datapath \
                --partition_method dirichlet \
                --save_dir $log_dir \
                --alpha $alpha \
                --seed $seed \
                --batch_size 64 \
                --gpu \
                --debug \
                --lr 5e-4 \
                --epochs 50 \
                --hidden_dim $hidden_dim \
                --encode_dim 2000 \
                --optimizer Adam

            end=`date +%s`
            runtime=$((end-start))
            # Print time in hh:mm:ss
            echo "==> Time taken: $(($runtime / 3600 )):$((($runtime / 60) % 60)):$(( $runtime % 60 ))"
        
        done
    done
done
